// Add particle background animation
function createParticles() {
    const hero = document.querySelector('.hero');
    const particlesContainer = document.createElement('div');
    particlesContainer.style.cssText = `
        position: absolute;
        top: 0;
        left: 0;
        width: 100%;
        height: 100%;
        pointer-events: none;
        z-index: 1;
    `;
    
    for (let i = 0; i < 50; i++) {
        const particle = document.createElement('div');
        particle.style.cssText = `
            position: absolute;
            width: 4px;
            height: 4px;
            background: rgba(102, 126, 234, 0.3);
            border-radius: 50%;
            animation: float 3s ease-in-out infinite;
            animation-delay: ${Math.random() * 3}s;
        `;
        
        particle.style.left = Math.random() * 100 + '%';
        particle.style.top = Math.random() * 100 + '%';
        
        particlesContainer.appendChild(particle);
    }
    
    const style = document.createElement('style');
    style.textContent = `
        @keyframes float {
            0%, 100% { transform: translateY(0px) rotate(0deg); opacity: 1; }
            33% { transform: translateY(-30px) rotate(120deg); opacity: 0.8; }
            66% { transform: translateY(30px) rotate(240deg); opacity: 0.6; }
        }
    `;
    
    if (!document.querySelector('style[data-particles]')) {
        style.setAttribute('data-particles', 'true');
        document.head.appendChild(style);
    }
    
    hero.style.position = 'relative';
    hero.appendChild(particlesContainer);
}

// Initialize particles on load
document.addEventListener('DOMContentLoaded', createParticles);
