// Contact form handling
const contactForm = document.getElementById('contact-form');
if (contactForm) {
    contactForm.addEventListener('submit', (e) => {
        e.preventDefault();
        
        // Get form data
        const formData = new FormData(contactForm);
        const name = formData.get('name');
        const email = formData.get('email');
        const subject = formData.get('subject');
        const message = formData.get('message');
        
        // Basic validation
        if (!name || !email || !subject || !message) {
            alert('Please fill in all fields.');
            return;
        }
        
        if (!isValidEmail(email)) {
            alert('Please enter a valid email address.');
            return;
        }
        
        // Show success message (you would typically send this to a server)
        alert('Thank you for your message! I\'ll get back to you soon.');
        contactForm.reset();
    });
}

// Email validation function
function isValidEmail(email) {
    const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
    return emailRegex.test(email);
}

// Profile image fallback
document.addEventListener('DOMContentLoaded', () => {
    const profileImg = document.getElementById('profile-img');
    const heroAvatar = document.querySelector('.hero-avatar');
    
    if (profileImg && heroAvatar) {
        // Try to load the image
        profileImg.onload = function() {
            // Image loaded successfully, hide the background
            heroAvatar.style.background = 'none';
            profileImg.style.display = 'block';
        };
        
        profileImg.onerror = function() {
            // Image failed to load, show initials
            profileImg.style.display = 'none';
            heroAvatar.style.background = 'linear-gradient(135deg, #667eea 0%, #764ba2 100%)';
            // Add the initials back via CSS
            const style = document.createElement('style');
            style.textContent = '.hero-avatar::before { content: "SC"; }';
            document.head.appendChild(style);
        };
        
        // Force check if image is already loaded (cached)
        if (profileImg.complete && profileImg.naturalHeight !== 0) {
            profileImg.onload();
        }
    }
});

// Typing animation for hero title
function typeWriter(element, text, speed = 100) {
    let i = 0;
    element.innerHTML = '';
    
    function type() {
        if (i < text.length) {
            element.innerHTML += text.charAt(i);
            i++;
            setTimeout(type, speed);
        }
    }
    
    type();
}

